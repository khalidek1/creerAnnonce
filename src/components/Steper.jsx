import React, { useEffect, useState, useRef } from "react";

const Steper = ({ step, CurrentStep }) => {
  const [newStep, setnewStep] = useState([]);
  const stepref = useRef();

  const updateStep = (stepNumber, step) => {
    const newStep = [...step];
    let count = 0;
    while (count < newStep.length) {
      if (count === stepNumber) {
        newStep[count] = {
          ...newStep[count],
          highlighted: true,
          selected: true,
          completed: true,
        };
        count++;
      } else if (count < stepNumber) {
        newStep[count] = {
          ...newStep[count],
          highlighted: false,
          selected: false,
          completed: false,
        };
        count++;
      } else {
        newStep[count] = {
          ...newStep[count],
          highlighted: false,
          selected: false,
          completed: false,
        };
        count++;
      }
    }
    return newStep;
  };

  useEffect(() => {
    //create object
    const stepState = step.map((step, index) =>
      Object.assign(
        {},
        {
          description: step,
          completed: false,
          highlighted: index === 0 ? true : false,
          selected: index === 0 ? true : false,
        }
      )
    );
    stepref.current = stepState;
    const current = updateStep(CurrentStep - 1, stepref.current);
    setnewStep(current);
  }, [step, CurrentStep]);

  const displaystep = newStep.map((step, index) => {
    return (
      <div
        key={index}
        className={
          index != newStep.length - 1
            ? " w-full flex  items-center "
            : "flex  items-center"
        }
      >
        <div className=" relative flex flex-col items-center text-teal-600">
          <div
            className={`rounded-full transition duration-500 ease-in-out border-2 
         border-gray-300 h-12 w-12 flex justify-center py-3 items-center
         ${
           step.selected
             ? " bg-green-500 text-white font-bold border-green-600"
             : ""
         } `}
          >
            {step.completed ? (
              <span className="text-white font-bold text-xl">&#10003;</span>
            ) : (
              index + 1
            )}
          </div>
          <div
            className={`absolute top-0 text-center mt-16 w-32 text-xs font-medium uppercase 
            ${step.highlighted ? " text-gray-900 " : "text-gray-400 "} `}
          >
            {step.description}
          </div>
        </div>
        <div
          className={`flex-auto border-t-2  transition duration-500 ease-in-out
          ${step.completed ? " border-green-600 " : "border-gray-300 "} `}
        ></div>
      </div>
    );
  });

  return (
    <div className="mx-4 py-4 flex justify-between items-center">
      {displaystep}
    </div>
  );
};

export default Steper;
